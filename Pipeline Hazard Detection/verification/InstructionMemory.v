module InstructionMemory(
    input wire [31:0] A,
    output reg [31:0] RD
);

always @(*) begin
    case (A[31:0]) 
        32'd0: RD = 32'b00000000010000000010000010000011; // lw x1 x0(4)
        32'd4: RD = 32'b00000000000000000000000000110011; // nop
        32'd8: RD = 32'b00000000000000000000000000110011; //nop
        //32'd12: RD = 32'b00000000000000000000000000110011; //nop
        32'd12: RD = 32'b00000000000000001000000100110011; //add x2, x1, x0
        32'd16: RD = 32'b00000000000000000000000000110011; // nop
        32'd20: RD = 32'b00000000000000000000000000110011; //nop
        32'd24: RD = 32'b00000000000000000000000000110011; //nop
        

    endcase
end

endmodule


